# character print from string
country = "Bangladesh"
for char in country:
    print(char)
print()

# number print from specific conditions
for index in range(5):
    print(index)
print()

# start from 10 to 20
for num in range(10, 21):
    print(num)
print()

# reverse number from 20 to 1
for num in range(20,0,-1):
    print(num)
print()

# start from 5 and step 5
for i in range(5, 46, 5):
    print(i)
print()
